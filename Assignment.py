#This program will help compare and determin each side of the triangle and figure if its eqilateral or not equilateral.

a = int(input("Insert the first side of the triangle: "))

b = int(input("Insert the second side of the triangle: "))

c = int(input("Insert the second side of the triangle: "))

if a==c and c==b:

    print("\nThe triangle would be considered equilateral.")

else:

    print("\nThe triangle would not be considered equilateral.")

    
